# include "webserv/request.hpp"

// * Constructor ****************************************************************************************************
Request::Request() 
    : location(NULL),
    upload_file_fd(-1)
{}
// ******************************************************************************************************************

// * Destructor *****************************************************************************************************
Request::~Request() {
	if (upload_file_fd != -1) {
		close(upload_file_fd);
        if (state != READY) {
            unlink(upload_file_name.c_str());
        }
    }
}
// ******************************************************************************************************************